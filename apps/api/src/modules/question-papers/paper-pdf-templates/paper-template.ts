const OPTION_LABELS = ['A', 'B', 'C', 'D', 'E', 'F'];

/**
 * Tiny wrapper around `React.createElement` that accepts a typed props object
 * along with the React-special `key` attribute. We use `unknown` for the
 * inner type so the caller can pass either react-pdf style props or
 * react-pdf-svg props without TS quibbling at the boundary — the templates
 * stay readable, and the renderer enforces the real prop contract at render
 * time.
 */
function el(
  type: React.ElementType,
  props: Record<string, unknown> & { key?: string | number } = {},
  ...children: React.ReactNode[]
): React.ReactElement {
  // React.createElement's typings reject our props because `key` is special
  // and react-pdf's prop types don't include it. We're calling the runtime
  // contract which DOES accept `key` in the props bag.
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return React.createElement(type as any, props as any, ...children);
}

export function PaperTemplate(ctx: PaperPdfContext): React.ReactElement {
  const { paper, classroom, institution, source } = ctx;

  const footerText =
    `Generated by Vaasenk · Syllabus: "${source.syllabusName}" v${source.syllabusVersion}` +
    (source.samplePaperNames.length > 0
      ? ` · Patterned after ${source.samplePaperNames.join(', ')}`
      : '');

  const pageNumberRenderer = (info: {
    pageNumber: number;
    totalPages: number;
  }): string => `Page ${info.pageNumber} of ${info.totalPages}`;

  return el(
    Document,
    {},
    el(
      Page,
      { size: 'A4', style: styles.page },

      // Header
      el(
        View,
        { style: styles.header, fixed: true },
        el(Text, { style: styles.institutionName }, institution.name),
        el(Text, { style: styles.paperTitle }, paper.title),
        el(
          View,
          { style: styles.metaRow },
          el(
            Text,
            { style: styles.metaCell },
            `Class: ${classroom.className}${classroom.sectionName ? ' — ' + classroom.sectionName : ''}`,
          ),
          el(
            Text,
            { style: { ...styles.metaCell, textAlign: 'center' } },
            `Subject: ${classroom.subjectName}`,
          ),
          el(
            Text,
            { style: { ...styles.metaCell, ...styles.metaCellRight } },
            `Max Marks: ${paper.totalMarks}` +
              (paper.durationMinutes
                ? ` · Time: ${paper.durationMinutes} min`
                : ''),
          ),
        ),
      ),

      // Instructions
      paper.structuredContent.instructions &&
        el(
          View,
          { style: styles.instructionsBlock },
          el(Text, {}, paper.structuredContent.instructions),
        ),

      // Sections
      ...paper.structuredContent.sections.map((section, sIdx) =>
        el(
          View,
          { key: `s-${sIdx}`, wrap: true },
          el(Text, { style: styles.sectionHeader }, section.name),
          section.instructions
            ? el(Text, { style: styles.sectionInstructions }, section.instructions)
            : null,
          ...section.questions.map((q, qIdx) =>
            el(
              View,
              { key: `s-${sIdx}-q-${qIdx}`, style: styles.question, wrap: false },
              el(
                View,
                { style: styles.questionRow },
                el(Text, { style: styles.questionNumber }, `${qIdx + 1}.`),
                el(
                  View,
                  { style: styles.questionBody },
                  el(
                    Text,
                    { style: styles.questionText },
                    `${q.text}   `,
                    el(
                      Text,
                      { style: styles.questionMarks },
                      `[${q.marks} mark${q.marks === 1 ? '' : 's'}]`,
                    ),
                  ),
                  q.options && q.options.length > 0
                    ? el(
                        View,
                        { style: styles.optionsBlock },
                        ...q.options.map((opt, oIdx) =>
                          el(
                            Text,
                            { key: `opt-${oIdx}`, style: styles.optionLine },
                            `${OPTION_LABELS[oIdx] ?? oIdx + 1}. ${opt}`,
                          ),
                        ),
                      )
                    : null,
                ),
              ),
            ),
          ),
        ),
      ),

      // Footer (rendered fixed on every page).
      el(Text, { style: styles.footer, fixed: true }, footerText),
      el(Text, { style: styles.pageNumber, render: pageNumberRenderer, fixed: true }),
    ),
  );
}
