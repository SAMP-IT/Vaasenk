/**
 * Question paper PDF template — Sprint 5 PROMPT 20.
 *
 * Renders the structured paper into a clean, printable, exam-style PDF.
 * No Vaasenk gradients — printable black ink only. Header with metadata,
 * numbered sections, numbered questions (with MCQ options indented), and a
 * footer with the syllabus attribution + Vaasenk wordmark.
 *
 * Uses `React.createElement` directly (no JSX) so the file can live in the
 * existing NestJS `commonjs` + non-JSX tsconfig without configuration churn.
 * @react-pdf/renderer accepts plain React elements identically to JSX output.
 */

